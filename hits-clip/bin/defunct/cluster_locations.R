setwd("/Users/brownj/projects/hitsclip/data/")
dstart = read.table("BMEC.peaklocs.start_codon.txt")
dstop = read.table("BMEC.peaklocs.stop_codon.txt")

par(mfrow=c(1, 2))
sstart = subset(dstart, V1 <= 10000 & V1 >= -10000)
sstop = subset(dstop, V1 <= 10000 & V1 >= -10000)
start_d <- density(sstart$V1)
stop_d <- density(sstop$V1)
plot(start_d, main="hMSC start codon", xlab=paste("Distance (bp), N=", start_d$n, sep=''))
text(x, start_d$n)
polygon(start_d, col="red", border="black")
rug(sstart$V1, col="black")
plot(stop_d, main="hMSC stop codon", xlab=paste("Distance (bp), N=", stop_d$n, sep=''))
polygon(stop_d, col="blue", border="black")
rug(sstop$V1, col="black")

